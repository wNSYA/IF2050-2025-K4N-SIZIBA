package org.example.uisiziba;

public class WHOEntry {
    private final double p3;
    private final double p85;

    public WHOEntry(double p3, double p85) {
        this.p3 = p3;
        this.p85 = p85;
    }

    public double getP3() {
        return p3;
    }

    public double getP85() {
        return p85;
    }
}
